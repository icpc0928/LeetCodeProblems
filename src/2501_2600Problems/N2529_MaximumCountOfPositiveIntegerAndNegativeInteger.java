    static class Solution {
        public int maximumCount(int[] nums) {
            Arrays.binarySearch(nums, 1);
            int negCount = 0;
            int posCount = 0;
            boolean neg = true;
            boolean pos = true;

            for(int i = 0; i < nums.length; i++){
                if(nums[i] < 0){
                    negCount++;
                }else{
                    neg = false;
                }
                if(nums[nums.length - 1 - i] > 0){
                    posCount++;
                }else{
                    pos = false;
                }
                if(!pos && !neg){
                    break;
                }
            }
            return Math.max(negCount, posCount);

        }
    }
